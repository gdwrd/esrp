// Package group SRP Group Parameters
package group

import (
	"math/big"

	v "github.com/nsheremet/esrp/value"
)

// Group struct
//
// Provides:
// N - A large safe prime (N = 2q+1, where q is prime)
// G - A generator module N
// PrimeLength - for debugging
//
// The predefined prime groups are taken from https://tools.ietf.org/html/rfc5054#appendix-A
// 1024 and 1536 bit groups are for compatibility and RFC5054 compliance only.
// It is possible to use custom groups, but they should come from
// a trusted source (see Trapdoor attack)
//
// It's STRONGLY RECOMMENDED to use 2048 and upper groups (see Logjam attack)
type Group struct {
	PrimeLength int
	G           v.Value
	N           v.Value
}

// New function
//
// Constructor:
//
// Params:
// - primeLength {int} prime length
// - g					 {int} generator (g)
// - nn					 {int} large safe prime (N)
func New(primeLength, g int, nn string) Group {
	return Group{
		PrimeLength: primeLength,
		G:           v.New(big.NewInt(int64(g))),
		N:           v.New(nn),
	}
}

